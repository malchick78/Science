/**
 * Express1.java provides a 'laboratory' for exploring Java expressions.
 *
 **********************************************************************/

public class Express1
{ 
   public static void main(String[] args)
   {
      int i, j;
      i = 10;
      j = 5;

      System.out.println(i == j);
	  System.out.println(i != j);
	  System.out.println(i < j);
	  System.out.println(i > j);
	  System.out.println(i <= j);
	  System.out.println(i >= j);
   }
}